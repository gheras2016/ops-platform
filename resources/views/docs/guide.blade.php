<h2>6. دورة الشراء والاعتماد المالي</h2>
<p>عند الحاجة لشراء قطعة، يمرّ الطلب بسلسلة اعتماد واضحة قبل التنفيذ:</p>
<table>
    <tr><th style="width:24%">المرحلة</th><th>المسؤول</th></tr>
    <tr><td>إنشاء طلب الشراء</td><td>تلقائي عند اعتماد رئيس القسم لقطعة غير متوفّرة، أو يدوياً.</td></tr>
    <tr><td>اعتماد إداري</td><td>مدير التشغيل (المستوى الإداري الأعلى).</td></tr>
    <tr><td>اعتماد مالي</td><td>مدير المالية.</td></tr>
    <tr><td>التنفيذ والاستلام</td><td>مدير المخزون يستلم القطعة، فتُضاف للكتالوج وتُصرف للبلاغ.</td></tr>
</table>
<div class="note"><b>تنبيه ذكي:</b> يعرض النظام شارة عدّاد بجانب «طلبات الشراء» في القائمة الجانبية لكل من ينتظر إجراءه، حتى لا يتأخّر أي طلب.</div>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> طلبات الشراء والتوريد &nbsp;|&nbsp; <b>المسار:</b> <code>/purchase-requests</code></div>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> تفاصيل طلب الشراء وسلسلة الاعتماد &nbsp;|&nbsp; <b>المسار:</b> <code>/purchase-requests/{رقم}</code></div>

