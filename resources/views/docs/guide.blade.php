<h2>1. نظرة عامة على النظام</h2>
<p class="lead">نظام يحوّل عملية الصيانة من مكالمات وأوراق متفرّقة إلى دورة رقمية منظّمة ومتتبَّعة بالكامل: من لحظة رفع البلاغ حتى إغلاقه واعتماده، مروراً بإدارة الفنيين وقطع الغيار والمشتريات والتقارير.</p>
<p>يعمل النظام لعدّة شركات في آنٍ واحد مع عزل تامّ لبيانات كل شركة، وهوية بصرية (ألوان وشعار) مستقلّة لكل عميل.</p>

<div class="note"><b>الفكرة في سطر:</b> كل بلاغ يمرّ بمسار واضح ومسؤول معروف في كل مرحلة، وكل إجراء يُسجَّل في سجلّ زمني لا يمكن التلاعب به.</div>

{{-- ============================================================= --}}
<h2>2. الأدوار والمستخدمون</h2>
<p>لكل مستخدم دور يحدّد ما يراه وما يستطيع فعله:</p>
<table>
    <tr><th style="width:22%">الدور</th><th>المسؤولية</th></tr>
    <tr><td><span class="role">المبلّغ</span></td><td>يرفع البلاغ ويصف العطل ويتابع حالته.</td></tr>
    <tr><td><span class="role">رئيس القسم</span></td><td>يستقبل بلاغات قسمه، يُسندها للفنيين، يحدّد الأولوية والموعد، ويعتمد الإنجاز.</td></tr>
    <tr><td><span class="role">الفني</span></td><td>ينفّذ المهام المسندة إليه: قبول، بدء، إيقاف مؤقّت، إنجاز، وتسجيل قطع الغيار.</td></tr>
    <tr><td><span class="role">مدير المخزون</span></td><td>يدير قطع الغيار، يصرف المطلوب، ويحوّل النقص إلى شراء.</td></tr>
    <tr><td><span class="role">مدير المالية</span></td><td>يعتمد طلبات الشراء مالياً.</td></tr>
    <tr><td><span class="role">مدير النظام</span></td><td>يدير شركته بالكامل (مستخدمين، أقسام، إعدادات، تقارير).</td></tr>
    <tr><td><span class="role">مدير المنصّة</span></td><td>يدير جميع الشركات (إنشاء، إيقاف، تصدير). مالك النظام.</td></tr>
</table>

