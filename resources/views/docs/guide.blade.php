<h2>5. دورة قطع الغيار والمخزون</h2>
<p>مرتبطة مباشرة بالبلاغ. عندما يحتاج الفني قطعة:</p>
<table>
    <tr><th style="width:22%">الخطوة</th><th>التفاصيل</th></tr>
    <tr><td>طلب القطعة</td><td>الفني ينشئ طلب صرف من داخل البلاغ (قطعة من الكتالوج أو قطعة جديدة بالاسم).</td></tr>
    <tr><td>الاعتماد</td><td>رئيس القسم يعتمد طلب الصرف.</td></tr>
    <tr><td>الصرف</td><td>إن كانت متوفّرة بالمخزون → يصرفها مدير المخزون وتُخصم الكمية وتُسجَّل تكلفتها على البلاغ.</td></tr>
    <tr><td>التحويل للشراء</td><td>إن كانت غير متوفّرة أو خارج الكتالوج → تتحوّل تلقائياً إلى دورة شراء (القسم 6).</td></tr>
</table>
