<h2>3. الدخول إلى النظام</h2>
<p>يدخل كل مستخدم ببريده وكلمة مروره، فيُعرَض له تلقائياً ما يخصّ دوره وشركته فقط. الحسابات أو الشركات الموقوفة يُمنع دخولها فوراً.</p>

<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> تسجيل الدخول &nbsp;|&nbsp; <b>المسار:</b> <code>/login</code></div>

<p>بعد الدخول تظهر <b>لوحة التحكم</b> بمؤشرات مخصّصة حسب الدور (عدد البلاغات المفتوحة، قيد التنفيذ، المتأخّرة، المُغلقة…).</p>

<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> لوحة التحكم &nbsp;|&nbsp; <b>المسار:</b> <code>/dashboard</code></div>

{{-- ============================================================= --}}
<h2>4. دورة حياة البلاغ (خطوة بخطوة)</h2>
<p>هذه هي العملية الجوهرية في النظام. يمرّ البلاغ بالمراحل التالية:</p>

<h3><span class="step">1</span> رفع البلاغ &nbsp; <span class="role">المبلّغ</span></h3>
<p>يصف الموظف العطل، يختار القسم المختص والموقع (مبنى ← دور ← غرفة)، ويرفق صوراً عند الحاجة. لا يُطلب منه تقدير مدة الإنجاز — فهذا من مهمّة رئيس القسم.</p>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> رفع بلاغ جديد &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets/create</code></div>

<h3><span class="step">2</span> استقبال البلاغ والإسناد &nbsp; <span class="role">رئيس القسم</span></h3>
<p>يظهر البلاغ في قائمة بلاغات القسم بحالة «مفتوحة». يراجعه رئيس القسم، يحدّد <b>الأولوية</b> و<b>الموعد المتوقّع للإنجاز</b>، ويُسنده إلى أحد فنيي قسمه.</p>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> قائمة التذاكر &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets</code></div>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> نافذة إسناد البلاغ لفني (من صفحة تفاصيل البلاغ) &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets/{رقم}</code> ثم زر «إسناد»</div>

<h3><span class="step">3</span> القبول وبدء التنفيذ &nbsp; <span class="role">الفني</span></h3>
<p>يصل الفني إشعار بالمهمة. يقبلها فتصبح «مقبولة»، ثم يبدأ العمل فتصبح «قيد التنفيذ». يستطيع تحديث <b>نسبة الإنجاز</b> (0–100%) أثناء العمل.</p>

<h3><span class="step">4</span> الإيقاف المؤقّت عند الحاجة &nbsp; <span class="role">الفني</span></h3>
<p>إذا تعذّر إكمال العمل (مثلاً بانتظار قطعة غيار)، يوقف الفني البلاغ مؤقّتاً مع <b>تسجيل السبب</b>، ثم يستأنف لاحقاً. كل إيقاف واستئناف يُوثَّق.</p>

<h3><span class="step">5</span> متابعة البلاغ والسجل الزمني &nbsp; <span class="role">الجميع</span></h3>
<p>صفحة تفاصيل البلاغ تعرض الحالة الحالية، نسبة الإنجاز، <b>السجل الزمني الكامل</b> (من فعل ماذا ومتى)، التعليقات، والمرفقات — فيتابع المبلّغ ورئيس القسم كل تطوّر لحظياً.</p>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> تفاصيل البلاغ (الحالة + شريط الإنجاز + السجل الزمني) &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets/{رقم}</code></div>

<h3><span class="step">6</span> الإنجاز وتسجيل القطع المستخدمة &nbsp; <span class="role">الفني</span></h3>
<p>عند انتهاء العمل، يكتب الفني ملخّص الحل ويسجّل <b>قطع الغيار المستخدمة</b> (من المخزون: تُخصم تلقائياً / خارج الكتالوج: تُسجَّل بالاسم)، ثم يرفع البلاغ كـ «منجز» بانتظار الاعتماد.</p>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> نافذة إنهاء البلاغ وتسجيل القطع (من صفحة تفاصيل البلاغ) &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets/{رقم}</code> ثم زر «إنهاء»</div>

<h3><span class="step">7</span> الاعتماد والإغلاق &nbsp; <span class="role">رئيس القسم</span></h3>
<p>يراجع رئيس القسم العمل المنجز: إمّا <b>يعتمده</b> فيُغلق البلاغ نهائياً، أو <b>يرفضه</b> مع ملاحظة فيعود للفني لإعادة المعالجة. يصل الجميع إشعار بالنتيجة.</p>

<div class="note"><b>لوحة المهام (Board):</b> يوفّر النظام عرضاً بصرياً للبلاغات موزّعة حسب حالتها مع إجراءات سريعة — مفيد للفنيين ورؤساء الأقسام.</div>
<div class="shot">📷 لقطة شاشة مطلوبة &mdash; <b>الصفحة:</b> لوحة المهام &nbsp;|&nbsp; <b>المسار:</b> <code>/tickets/board</code></div>

